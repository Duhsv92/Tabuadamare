// ─── SUNRISE/SUNSET (NOAA simplified for Recife: 8.057°S, 34.867°W) ───
function sunTimes(dateStr) {
  const lat = -8.057;
  const lng = -34.867;
  const tzOff = -3; // UTC-3
  const d = new Date(dateStr + "T12:00:00");
  const start = new Date(d.getFullYear(), 0, 1);
  const dayOfYear = Math.floor((d - start) / 86400000) + 1;

  const gamma = (2 * Math.PI / 365) * (dayOfYear - 1);
  const eqTime = 229.18 * (0.000075 + 0.001868 * Math.cos(gamma) - 0.032077 * Math.sin(gamma)
    - 0.014615 * Math.cos(2 * gamma) - 0.040849 * Math.sin(2 * gamma));
  const decl = 0.006918 - 0.399912 * Math.cos(gamma) + 0.070257 * Math.sin(gamma)
    - 0.006758 * Math.cos(2 * gamma) + 0.000907 * Math.sin(2 * gamma)
    - 0.002697 * Math.cos(3 * gamma) + 0.00148 * Math.sin(3 * gamma);

  const latRad = lat * Math.PI / 180;
  const ha = Math.acos(
    (Math.cos(90.833 * Math.PI / 180) / (Math.cos(latRad) * Math.cos(decl)))
    - Math.tan(latRad) * Math.tan(decl)
  ) * 180 / Math.PI;

  const solarNoon = (720 - 4 * lng - eqTime + tzOff * 60) / 60;
  const sunriseH = solarNoon - ha / 15;
  const sunsetH = solarNoon + ha / 15;

  const toHM = (h) => {
    const hrs = Math.floor(h);
    const mins = Math.round((h - hrs) * 60);
    return `${String(hrs).padStart(2, "0")}:${String(mins).padStart(2, "0")}`;
  };

  return { sunrise: toHM(sunriseH), sunset: toHM(sunsetH) };
}

