    const lows = tideData.events.filter(e => e.type === "low");
    const maxH = Math.max(...highs.map(e => e.h));
    const minL = Math.min(...lows.map(e => e.h));
    const nowMs = now - new Date(today + "T00:00:00");
    const nowMinPassed = nowMs / 60000;
    const nextHigh = tideData.events.find(e => e.type === "high" && e.min > nowMinPassed);
    const nextLow = tideData.events.find(e => e.type === "low" && e.min > nowMinPassed);
